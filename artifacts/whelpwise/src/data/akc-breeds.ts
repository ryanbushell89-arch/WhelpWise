// Flat sorted list — use this for dropdowns and search.
// Each entry carries the parent group name for display.
export const AKC_ALL_BREEDS: { name: string; group: string }[] =
  AKC_BREED_GROUPS
    .flatMap(g => g.breeds.map(name => ({ name, group: g.name })))
    .sort((a, b) => a.name.localeCompare(b.name));
